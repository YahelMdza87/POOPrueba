
package Ejemplos;

public class Ejemplo01Operaciones {
    public static double suma (double n1, double n2){
        double respuesta;
        respuesta=n1+n2;
        return respuesta;
    
}
    public static double resta (double n1, double n2){
         double respuesta;
        respuesta=n1-n2;
        return respuesta;
        
    }
     public static double multiplicacion (double n1, double n2){
         double respuesta;
        respuesta=n1*n2;
        return respuesta;
        
    }
      public static double division (double n1, double n2){
         double respuesta;
        respuesta=n1/n2;
        return respuesta;
        
    }
    
}
